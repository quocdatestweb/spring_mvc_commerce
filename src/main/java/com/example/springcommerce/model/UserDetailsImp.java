package com.example.springcommerce.model;

import java.util.Collection;
import java.util.List;

import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import com.example.springcommerce.emun.Role;

import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.EnumType;
import jakarta.persistence.Enumerated;
import jakarta.persistence.Id;
import jakarta.persistence.Table;
import jakarta.validation.constraints.NotBlank;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;

@Data
@Builder
@NoArgsConstructor
@AllArgsConstructor
@Entity
@Table(name = "user")
public class UserDetailsImp implements UserDetails {

    @Id
    @Column(name = "User_Name", length = 20, nullable = false)
    private String userName;

    private String firstName;

    private String lastName;

    private String address;

    private String phone;

    // private String email;

    @NotBlank(message = "Password is required")
    @Column(name = "Encryted_Password", length = 128, nullable = false)
    private String password;
    @Column(name = "Active", length = 1, nullable = false)
    private boolean active;

    @Enumerated(EnumType.STRING)
    private Role role;

    public UserDetailsImp(Long userId) {
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return List.of(new SimpleGrantedAuthority(role.name()));
    }

    public String getUserName() {
        return userName;
    }

    @Override
    public String getUsername() {
        return userName;
    }

    @Override
    public String getPassword() {
        return password;
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return true;
    }

    @Override
    public String toString() {
        return "UserDetailsImp [userName=" + userName + ", firstName=" + firstName + ", lastName=" + lastName
                + ", address=" + address + ", phone=" + phone + ", password=" + password + ", active=" + active
                + ", role=" + role + "]";
    }

}
